# More than just aligning notes to the gird at 100%, this quantize feature 
# is smart. Often in a MIDI recording taken from live playing most of the 
# notes will be fine. It may be only 5% or less that are off in timing. 
# This tool can analyze the surrounding notes and identify "rhythmic grooves" 
# so to speak, so it knows which notes are close enough in time, and which 
# ones are really off.

# The tool also has a % setting, where the user can adjust an amount from
# 1 to 100.  0 being no effect of quantization at all,
# 100 being complete quantization.

# As well as being intelligent and being able to find only the notes that 
# are off time, the tool also has the standard "quantize to exact note length" 
# options. It should have all the same note lengths that the grid can have: a user 
# switchable note value, such as one bar (whole note), 1/4 note, 18th note, etc.
# As well as complex, odd, and mixed meters.
